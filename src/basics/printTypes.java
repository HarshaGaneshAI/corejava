package basics;

public class printTypes {
    public static void main(String[] args) {
        System.err.println("HI"); //err can print our output in red colour.
        System.out.println("I am");
        System.out.print("Helo");
        System.out.print(" no line skipped ");
        System.out.printf("My name is %s and my age %d","John",22);
    }
}
/*
print = output stays in the same line
println= output can print the statement in the new line
printf= defining %d and %s type of statement (formatting strings)
 */