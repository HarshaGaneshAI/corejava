package basic;

public class testdatatypes {
    public static void main(String[] args){
        int i = 899;
        int j = 498;
        System.out.println(i+j);
        System.out.println(i-j);
        System.out.println(i*j);
        System.out.println(i/j);
        System.out.println(i%j);
        System.out.println(++i); //pre
        System.out.println(i++); //post
        System.out.println(i);
        System.out.println(--j);
        System.out.println(j--);
        System.out.println(j);
        System.out.println(i==j);
        System.out.println(i!=j);
        System.out.println(i>j);
        System.out.println(i<j);
        System.out.println(i>=j);
        System.out.println(i<=j);
        System.out.println(i < j && i==j);

    }
}
